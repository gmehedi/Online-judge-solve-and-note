using namespace std;
vector<int>prime;
bool sign[maxl+2];

void Prime()
{
    memset(sign,true,sizeof sign);

    for(int i=3; i*i<=maxl; i+=2)
    {
       if(sign[i]) for(int j=i*i; j<=maxl; j+=(i*2)) sign[j]=false;
    }
    prime.push_back(2);
    for(int i=3; i<=maxl; i+=2) if(sign[i]) prime.push_back(i);
}
int main()
{
    int t,cas=1;
    Prime();
    scanf("%d",&t);
    while(t--)
    {
        int n;
        scanf("%d",&n);
        ll ans=0;
        for(int i=0; i<n; i++)
        {
            int temp;
            scanf("%d",&temp);
            int ind=upper_bound(prime.begin(),prime.end(),temp)-prime.begin();
            ans+=prime[ind];
        }

        printf("Case %d: %lld Xukha\n",cas++,ans);
    }
    return 0;
}
